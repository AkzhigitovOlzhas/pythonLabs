# Для настольной игры используются карточки с номерами от 1 до N. Одна карточка
# потерялась. Найдите ее, зная номера оставшихся карточек.
# Дано число N, далее N − 1 номер оставшихся карточек (различные числа от 1 до N).
# Программа должна вывести номер потерянной карточки.
n5 = int(input('Введите кол-во карточек: '))
if n5 <= 1:
    print('Нельзя вводить число меньше или равное 1!')
else:
    lost_card = 0
    for a in range(1, n5 + 1):
        lost_card += a
    for a in range(1, n5):
        temp = int(input('Введите число: '))
        while n5 < temp or temp < 1:
            print('Введите число от 1 до', n5)
            temp = int(input('Введите число: '))
        lost_card -= temp
    print('Потерянная карточка: ', lost_card)
